package 연산자;

public class 비교연산자 {

	public static void main(String[] args) {
		// ==, !=, >=, >
		// 비교의 결과는 boolean(true/false)
		// 비교연산자는 기본형만 비교 가능.!
		// 정수, 실수, 문자1, 논리
		// String은 비교연산자로 비교 불가.
		int x = 100;
		int y = 200;
		System.out.println(x == y);//flase
		System.out.println(x != y);//true

	}

}
